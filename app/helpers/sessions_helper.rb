module SessionsHelper
  def log_in user
    session[:user_id] = user.id
  end

  def current_user
    @current_user ||= User.find_by id: session[:user_id]
  end

  def logged_in?
    current_user.present?
  end

  def log_out
    session.delete :user_id
    @current_user = nil
  end

  def check_logged_in
    unless logged_in?
      redirect_to login_path
      flash[:danger] = t "please_login"
    end
  end
end
